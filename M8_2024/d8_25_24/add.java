import java.util.*;

public class add {
    public static int add(int a, int b) {
        return a+b;
    }
    public static int mult(int a, int b) {
        return a*b;
    }

    public static void main(String[] args) {
        int a=10;
        int b=8;
         System.out.println(add(a, b));
         System.out.println(mult(a, b));
    }
}
